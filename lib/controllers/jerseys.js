const { Router } = require('express');
const { Jersey } = require('../models/Jersey');

module.exports = Router()
  .get('/:id', async (req, res, next) => {
    try {
      const data = await Jersey.getById(req.params.id);
      if (!data) {
        next();
      }
      res.json(data);
    } catch (e) {
      next(e);
    }
  })
  .get('/', async (req, res, next) => {
    try {
      const data = await Jersey.getAll();
      console.log(data);
      res.json(data);
    } catch (e) {
      next(e);
    }
  })
  .post('/', async (req, res, next) => {
    try {
      const data = await Jersey.insert(req.body);
      res.json(data);
    } catch (e) {
      next(e);
    }
  })
  .put('/:id', async (req, res, next) => {
    try {
      const data = await Jersey.updateById(req.params.id, req.body);
      res.json(data);
    } catch (e) {
      next(e);
    }
  })
  .delete('/:id', async (req, res, next) => {
    try {
      const data = await Jersey.delete(req.params.id);
      res.json(data);
    } catch (e) {
      next(e);
    }
  });
